package com.ssdut.KindGardenProject.pojo;

public class Skill {
    private Integer skillId;

    private String skillName;

    private Integer skillFlag;

    public Integer getSkillId() {
        return skillId;
    }

    public void setSkillId(Integer skillId) {
        this.skillId = skillId;
    }

    public String getSkillName() {
        return skillName;
    }

    public void setSkillName(String skillName) {
        this.skillName = skillName == null ? null : skillName.trim();
    }

    public Integer getSkillFlag() {
        return skillFlag;
    }

    public void setSkillFlag(Integer skillFlag) {
        this.skillFlag = skillFlag;
    }
}